/**
 * קטגוריזציה של מוצר לפי שמו
 * @param {string} name - שם המוצר
 * @returns {string} category_id (מ-config.CATEGORIES)
 */
export function categorize(name) {
  if (!name || typeof name !== 'string') return 'other';

  const normalized = name.trim().toLowerCase();

  // בדיקה לפי מילות מפתח
  for (const [categoryId, keywords] of Object.entries(CATEGORY_KEYWORDS)) {
    for (const keyword of keywords) {
      if (normalized.includes(keyword.toLowerCase())) {
        return categoryId;
      }
    }
  }

  return 'other';
}


/**
 * זיהוי יחידת מידה מועדפת למוצר
 * @param {string} name
 * @returns {string} 'kg' | 'units'
 */
export function detectUnit(name) {
  if (!name) return 'units';

  const normalized = name.trim().toLowerCase();

  for (const keyword of WEIGHT_PRODUCTS) {
    if (normalized.includes(keyword.toLowerCase())) {
      return 'kg';
    }
  }

  return 'units';
}


/**
 * ניסיון לחלץ כמות מטקסט חופשי
 * למשל: "2 חלב" → { quantity: 2, name: 'חלב' }
 *        "שלושה לחמים" → { quantity: 3, name: 'לחמים' }
 *        "חלב" → { quantity: 1, name: 'חלב' }
 *
 * @param {string} text
 * @returns {{ quantity: number, name: string, unit?: string }}
 */
export function extractQuantity(text) {
  if (!text || typeof text !== 'string') {
    return { quantity: 1, name: '' };
  }

  const trimmed = text.trim();
  const words = trimmed.split(/\s+/);

  if (words.length === 0) return { quantity: 1, name: trimmed };

  const firstWord = words[0];

  // בדיקה למספר בקידומת
  // למשל: "2 חלב" או "2.5 קג עגבניות"
  const numMatch = firstWord.match(/^(\d+(?:\.\d+)?)(kg|ק״ג|קג|gr|גרם)?$/i);
  if (numMatch) {
    const quantity = parseFloat(numMatch[1]);
    const unitHint = numMatch[2];
    const restOfName = words.slice(1).join(' ').trim();

    let unit = null;
    if (unitHint) {
      if (/kg|ק״ג|קג/i.test(unitHint)) unit = 'kg';
      if (/gr|גרם/i.test(unitHint)) unit = 'g';
    }

    return {
      quantity: quantity > 0 ? quantity : 1,
      name: restOfName || trimmed,
      unit: unit,
    };
  }

  // בדיקה למספר במילים
  if (HEBREW_NUMBERS[firstWord]) {
    return {
      quantity: HEBREW_NUMBERS[firstWord],
      name: words.slice(1).join(' ').trim() || trimmed,
    };
  }

  return { quantity: 1, name: trimmed };
}


/**
 * פרסור מלא של קלט משתמש למוצר מוכן להוספה
 * @param {string} input - טקסט חופשי שהמשתמש הקליד
 * @returns {{ name, quantity, unit, category } | null}
 */
export function parseUserInput(input) {
  if (!input || !input.trim()) return null;

  const { quantity, name, unit: unitFromQuantity } = extractQuantity(input);

  if (!name || name.length === 0) return null;

  const category = categorize(name);
  const unit = unitFromQuantity || detectUnit(name);

  return {
    name: name,
    quantity: quantity,
    unit: unit,
    category: category,
  };
}


